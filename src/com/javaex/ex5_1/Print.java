package com.javaex.ex5_1;

public class Print {
	//필드
	//필드에서 선언을 미리해두면 몇개, 어떤 자료형으로 쓸건지 그릇을 만들어둬야하는것이기때문에 선언하면 안되는거임
	//생성자
	
	//메서드 gs
	
	//메서드 일반
	public void printer(int va1) { //선언까지 된거임
		System.out.println(va1);
	}
	
	public void printer(boolean va2) {
		System.out.println(va2);
	}
	public void printer(double va3) {
		System.out.println(va3);
	}
	public void printer(String va4) {
		System.out.println(va4);
	}
}
